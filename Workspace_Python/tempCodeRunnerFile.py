print(10 > 3) #T 크다.
print(10 < 3) #F 작다.
print(10 >= 3) #T 크거나 같다.
print(10 <= 3) #F 작거나 같다.
print(10 != 10) #F 같지 않다.
print(10 == 10) #T 같다.

print(not(1 != 3)) #값을 반대로 출력. T이지만 not 수식에 의해 False 출력

print((3 > 0) and (3 < 5)) #T 그리고 연산자
print((3 >0) & (3 < 5))# &은 and와 의미가 같다.

print((3 > 0) or (3 > 5))# T 또는 연산자
print((3 > 0) | (3 > 5))# |은 or과 의미가 같다.

print(5 > 4 > 3) #T 연속으로 연산 가능.
print(5 > 4 > 7) #F 